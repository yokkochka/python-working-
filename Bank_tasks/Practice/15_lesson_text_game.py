import random

# Определение направлений движения
DIRECTIONS = ['вперед', 'влево', 'вправо']

# Определение загадок и ответов на них
RIDDLES = {
    "Чем больше отдаешь, тем больше у тебя есть. Кто это?": "время",
    "Животное, которое спит в снегу зимой и охотится на рыбу. Кто это?": "медведь",
    "Что можно увидеть с закрытыми глазами?": "сон",
    "Что идет по лесу и никогда не шагает?": "река"
}

# Определение событий
EVENTS = [
    "Вы нашли клад! Ваши запасы увеличились.",
    "Вы наткнулись на дикий зверь. Придется с ним сразиться!",
    "На вашем пути встречается доброжелательное существо. Оно помогает вам найти путь.",
    "Вы нашли выход из леса! Поздравляем с победой!"
]

def main():
    print("Добро пожаловать в игру 'Путешествие по Загадочному Лесу'!")
    print("Вы находитесь в загадочном лесу, и ваша цель - найти выход.")
    print("Чтобы двигаться, введите одно из направлений: вперед, влево, вправо.")

    while True:
        direction = input("Выберите направление: ").lower()
        if direction in DIRECTIONS:
            event = random.choice(EVENTS)
            print(event)
            if event == EVENTS[-1]:  # Проверка на выход из леса
                print("Спасибо за игру!")
                break
            elif "загадка" in event:
                riddle = random.choice(list(RIDDLES.keys()))
                print(riddle)
                answer = input("Ваш ответ: ").lower()
                if answer == RIDDLES[riddle]:
                    print("Правильно! Путь открыт.")
                else:
                    print("Неправильный ответ. Вы продолжаете свой путь.")
        else:
            print("Неверное направление! Попробуйте еще раз.")

if __name__ == "__main__":
    main()
